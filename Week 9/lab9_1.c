u8 data = 0xA;
u32 row, col, i;

	int main(void) {
  
  RCC->APB2ENR = 0x0000081D;
  GPIOC->CRL = 0x33333333;
  GPIOB->CRH = 0x33333333;

  TIM1->CR1 = 0x00;
  TIM1->CR2 = 0x00;
  TIM1->PSC = 0x07FF;
  TIM1->ARR = 0x2000;

  TIM1->DIER = 0x0001;
  NVIC->ISER[0] = 0x02000000;
  TIM1->CR1 |= 0x0001;

  row = 1;
  col = 0;
  i = 0;

  while(1){ ;}
} //end main

void TIM1_UP_IRQHandler(void) {
  if((TIM1->SR & 0x0001) != 0) {
    GPIOC->ODR = ~row;
    row = row << 1;
    col = font8x8[data][i];
    GPIOB->ODR = col << 8;
    i++;
    if(row == 0x100) {row = 1; i = 0;}
    TIM1->SR &= ~(1 << 0); //clear UIF
  }
}